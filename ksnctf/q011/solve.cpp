#include <iostream>

const int L = 21;

int main() {
    unsigned char ecx[L] = {0xF9, 0xB3, 0x5A, 0x4A, 0x18, 0xEA, 0x5D,
                            0x36, 0x99, 0xE2, 0x6,  0x3F, 0xA1, 0x20,
                            0x47, 0x1F, 0xF5, 0x86, 0xA3, 0xAC, 0xC7};
    unsigned char edx[L] = {0xBF, 0xFF, 0x1B, 0x0D, 0x47, 0xA7, 0x18,
                            0x4F, 0xCB, 0xD6, 0x5C, 0x59, 0x95, 0x61,
                            0x73, 0x57, 0xB3, 0xD1, 0x94, 0x9F, 0xAC};
    char flag[L + 1];

    for (int i = 0; i < L; i++) {
        flag[i] = ecx[i] ^ edx[i];
    }
    flag[L] = '\0';

    std::cout << flag << std::endl;

    return 0;
}
